const Audio = require('../../models/audio');

exports.updateAudio = async (req, res) => {
  try {
    const audio = await Audio.findByIdAndUpdate(req.params.id, req.body, {
      new: true,
      runValidators: true,
    });
    res.status(200).json({
      status: 'success',
      data: {
        audios: audio,
      },
    });
  } catch (error) {
    res.status(400).json({
      status: 'fail',
      message: error.message,
    });
  }
};
